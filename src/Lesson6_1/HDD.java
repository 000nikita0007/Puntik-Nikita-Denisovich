package Lesson6_1;

public class HDD {
    String name;
    int volume;
    String type;

    public HDD() {
    }

    @Override
    public String toString() {
        return "HDD{" +
                "name='" + name + '\'' +
                ", volume=" + volume +
                ", type='" + type + '\'' +
                '}';
    }
}
